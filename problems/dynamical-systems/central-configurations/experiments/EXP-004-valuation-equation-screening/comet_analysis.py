#!/usr/bin/env python3
"""EXP-004 comet analysis: per-component pointedness of a gfan prevariety output.

Input: a gfan _tropicalprevariety --usevaluation output (RAYS + MAXIMAL_CONES
sections). Components: union-find over maximal cones sharing rays (the connected
components of the polyhedral complex; JL25 call them comets). Per component, the
recession cone is generated by its unbounded rays (first coordinate 0). Pointedness
is certified EXACTLY:
  - POINTED certificate: a rational vector c with c . r < 0 STRICTLY for every
    unbounded generator r of the component (all generators in an open half-space
    implies no line in the cone). Candidate c comes from a float LP (least-squares
    style heuristic + perturbation); verification is exact over Fractions.
  - UNPOINTED certificate: an exact nonnegative rational combination of the
    generators summing to zero (found via exact search over small support sets:
    antipodal pairs and sympy nullspace vectors with sign-consistent supports).
Outputs a per-component table and a summary verdict per file.
Note gfan's 0th coordinate under --usevaluation: rays with first coord != 0 are
points of the complex; rays with first coord 0 are unbounded directions.
"""
import sys
from fractions import Fraction


def parse_sections(path):
    rays, cones = [], []
    section = None
    with open(path, encoding="utf-8", errors="replace") as fh:
        for line in fh:
            s = line.strip()
            if not s:
                section = None if section in ("RAYS", "MAXIMAL_CONES") else section
                continue
            if s in ("RAYS", "MAXIMAL_CONES", "MAXIMAL_CONES_OF_CLOSURE", "CONES",
                     "F_VECTOR", "LINEALITY_SPACE",
                     "AMBIENT_DIM", "DIM", "PURE", "MULTIPLICITIES", "SIMPLICIAL",
                     "ORTH_LINEALITY_SPACE", "LINEALITY_DIM", "MY_EULER",
                     "USED_RAYS", "N_RAYS"):
                section = s
                continue
            if section == "RAYS":
                row = s.split("#")[0].split()
                if row:
                    rays.append([Fraction(x) for x in row])
            elif section in ("MAXIMAL_CONES", "MAXIMAL_CONES_OF_CLOSURE", "CONES"):
                body = s.split("#")[0].strip()
                if body.startswith("{") and body.endswith("}"):
                    inner = body[1:-1].strip()
                    idx = [int(x) for x in inner.split()] if inner else []
                    cones.append(idx)
    return rays, cones


class DSU:
    def __init__(self, n):
        self.p = list(range(n))

    def find(self, a):
        while self.p[a] != a:
            self.p[a] = self.p[self.p[a]]
            a = self.p[a]
        return a

    def union(self, a, b):
        ra, rb = self.find(a), self.find(b)
        if ra != rb:
            self.p[ra] = rb


def components(rays, cones):
    n = len(rays)
    dsu = DSU(n)
    for cone in cones:
        for a, b in zip(cone, cone[1:]):
            dsu.union(a, b)
    comp = {}
    for i in range(n):
        comp.setdefault(dsu.find(i), []).append(i)
    # only components that appear in some cone (isolated rays still count as their own)
    return list(comp.values())


def pointed_certificate(gens):
    """Try to certify cone(gens) pointed: find rational c with c.g < 0 for all g.
    Returns (True, c) on success, (False, witness) when an exact nonnegative zero
    combination is found, (None, None) if undecided by these heuristics."""
    if not gens:
        return True, None
    d = len(gens[0])
    # heuristic candidates: -sum of generators (classic for cones in a halfspace),
    # then coordinate-descent refinements.
    def dot(c, g):
        return sum(ci * gi for ci, gi in zip(c, g))

    cands = []
    ssum = [Fraction(0)] * d
    for g in gens:
        for i, x in enumerate(g):
            ssum[i] -= x
    cands.append(ssum)
    cands.append([Fraction(-1)] * d)
    # refine: penalize violated generators iteratively (perceptron-style, exact)
    c = list(ssum)
    for _ in range(200):
        viol = [g for g in gens if dot(c, g) >= 0]
        if not viol:
            break
        for g in viol:
            c = [ci - gi for ci, gi in zip(c, g)]
    cands.append(c)
    for c in cands:
        if all(dot(c, g) < 0 for g in gens):
            return True, c
    # unpointedness: scaled-antipodal pair check (g_i = -lambda g_j, lambda > 0)
    def primitive_dir(g):
        nz = [(i, x) for i, x in enumerate(g) if x != 0]
        if not nz:
            return None, 0
        lead = nz[0][1]
        return tuple(x / abs(lead) for x in g), (1 if lead > 0 else -1)

    seen = {}
    for g in gens:
        d_, s_ = primitive_dir(g)
        if d_ is None:
            continue
        nd = tuple(-x for x in d_)
        if nd in seen:
            return False, ("scaled-antipodal", d_)
        seen[d_] = True
    # float perceptron with random restarts, exact verification of the rationalized c
    import random
    random.seed(20260724)
    fg = [[float(x) for x in g] for g in gens]
    for _restart in range(12):
        cf = [random.uniform(-1, 1) for _ in range(d)]
        for _ in range(20000):
            viol = [g for g in fg if sum(a * b for a, b in zip(cf, g)) >= 0]
            if not viol:
                break
            for g in viol:
                nrm = max(1e-12, sum(x * x for x in g) ** 0.5)
                for i in range(d):
                    cf[i] -= g[i] / nrm
        else:
            continue
        # rationalize and verify EXACTLY (this is the certificate)
        for denom in (10**6, 10**9, 10**12):
            cr = [Fraction(round(x * denom), denom) for x in cf]
            if all(dot(cr, g) < 0 for g in gens):
                return True, cr
    return None, None


def analyze(path):
    rays, cones = parse_sections(path)
    comps = components(rays, cones)
    n_pointed = n_unpointed = n_undecided = 0
    details = []
    for comp in comps:
        gens = [rays[i][:] for i in comp if rays[i][0] == 0]
        gens = [g[1:] for g in gens]  # drop the valuation coordinate
        ok, cert = pointed_certificate(gens)
        if ok is True:
            n_pointed += 1
        elif ok is False:
            n_unpointed += 1
            details.append(("UNPOINTED", len(comp), len(gens)))
        else:
            n_undecided += 1
            details.append(("UNDECIDED", len(comp), len(gens)))
    print(f"{path}: components: {len(comps)}, pointed: {n_pointed}, "
          f"unpointed: {n_unpointed}, undecided: {n_undecided}")
    for d in details[:10]:
        print("   ", d)
    return n_unpointed == 0 and n_undecided == 0


if __name__ == "__main__":
    ok_all = True
    for p in sys.argv[1:]:
        ok_all = analyze(p) and ok_all
    sys.exit(0 if ok_all else 1)
